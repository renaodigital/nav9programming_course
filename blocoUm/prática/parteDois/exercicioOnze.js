//  11 - Tendo como informação os três lados de um triângulo faça um programa
//  que imprima na tela se ele é EQUILÁTERO, ISÓSCELES ou ESCALENO.


// Triângulo Equilatero = 3 lados iguais
// Triângulo Isósceles = 2 lados iguais e um diferente
// Triângulo Escaleno = 3 lados diferentes


let x = 10;
let y = 10;
let z = 10;
let a = 22;
let b = 23;

if (x = y = z) {
    console.log("Esse é um Triângulo Equilatéro");
}

if (x = z < a) {
    console.log("Esse triângulo é Isósceles");
}

if (x > y < a) {
    console.log("Esse Triângulo é Escaleno");
}